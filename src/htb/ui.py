def header(title: str):
    console.print(f"\n[bold cyan]‚ēź‚ēź {title} ‚ēź‚ēź[/bold cyan]")


def ok(msg: str):
    console.print(f"  [green]‚úĒ[/green]  {msg}")


def warn(msg: str):
    console.print(f"  [yellow]‚ö†[/yellow]  {msg}")


def die(msg: str) -> NoReturn:
    _err_console.print(f"  [red]‚úė[/red]  {msg}")
    sys.exit(1)


def ask(question: str) -> bool:
    try:
        ans = input(f"  {question} [y/N] ")
        return ans.strip().lower() == "y"
    except EOFError:
        print()
        return False
    except KeyboardInterrupt:
        print()
        sys.exit(0)


def ask_input(prompt: str) -> str:
    try:
        return input(f"  {prompt} ").strip()
    except (EOFError, KeyboardInterrupt):
        print()
        sys.exit(0)
